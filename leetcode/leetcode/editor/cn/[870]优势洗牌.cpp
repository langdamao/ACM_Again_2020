class Solution {
public:
    vector<int> advantageCount(vector<int>& nums1, vector<int>& nums2) {
        multiset<int> s(nums1.begin(),nums1.end());
        vector<int> ret(nums1.size(),-1);
        int n = nums1.size();

        for (int i =0;i<n;i++){
            auto t = s.upper_bound(nums2[i]);
            if (t == s.end()) t = s.begin();
            ret[i] = *t;
            s.erase(t);
        }
        return ret;
    }
};
